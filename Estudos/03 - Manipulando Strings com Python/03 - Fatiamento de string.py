nome = 'Matheus Souza'

# Primeiro caractere:
print(f'{nome[0]}')

# Primeiro caractere ao sétimo:
print(f'{nome[:8]}')

# Oitavo caractere ao último:
print(f'{nome[8:]}')

# Terceiro ao oitavo:
print(f'{nome[2:8]}')

# Primeiro caractere ao último pulando de 2 em 2:
print(f'{nome[::2]}')

# Último caractere ao primeiro:
print(f'{nome[::-1]}')

