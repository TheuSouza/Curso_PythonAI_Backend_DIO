# Operadores de atribuição:

# Saldo recebe <- 500
saldo = 500
print(saldo)

# Saldo recebe <- Saldo + 200
saldo += 200
print(saldo)

# Saldo recebe <- Saldo - 100
saldo -= 100
print(saldo)

# Saldo recebe <- Saldo * 2
saldo *= 2
print(saldo)

# Saldo recebe <- Saldo // 5
saldo //= 5
print(saldo)

# Saldo recebe <- Saldo / 5
saldo /= 5
print(saldo)

# Saldo recebe <- Saldo % 450
saldo %= 40
print(saldo)

# Saldo recebe <- Saldo ** 2
saldo **= 2
print(saldo)