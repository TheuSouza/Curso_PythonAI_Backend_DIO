# Operadores aritméticos:

# Adição
print(1 + 1)

# Subtração
print(10 - 2)

# Multiplicação
print(4 * 3)

# Divisão
print(12 / 3)

# Divisão inteira
print(12 // 3)

# Módulo ou Resto da divisão
print(10 % 3)

# Exponenciação
print(2 ** 3)


# Precedência dos operadores:

'''

1º Parêntesis ()

2º Exponeciação **

3º Multiplicação * 
   Divisão / 
   Divisão inteira // 
   Resto da divisão %
(da esquerda para a direita)

4º Adição + 
   Subtração -
(da esquerda para a direita)

'''


# Exemplos:

print(10 - 5 * 2)

print((10 - 5) * 2)

print(10 ** 2 * 2)

print(10 ** (2 * 2))

print(10 / 2 * 4)

