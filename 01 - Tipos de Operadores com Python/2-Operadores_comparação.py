# Operadores de comparação:

saldo = 450
saque = 200

# saldo é Igual a saque? 
saldo == saque
print('saldo é Igual a saque? ',saldo == saque)

# saldo é Diferente de saque? 
saldo != saque
print('saldo é Igual a saque? ',saldo != saque)

# saldo é Maior que o saque? 
saldo > saque
print('saldo é Igual a saque? ',saldo > saque)

# saldo é Maior ou igual a saque? 
saldo >= saque
print('saldo é Igual a saque? ',saldo >= saque)

# saldo é Menor que o saque? 
saldo < saque
print('saldo é Igual a saque? ',saldo < saque)

# saldo é Menor ou igual a saque? 
saldo <= saque
print('saldo é Igual a saque? ',saldo <= saque)